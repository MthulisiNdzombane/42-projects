#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t a;
	size_t b;

	a = ft_strlen(s1);
	b = 0;
	while (s2[b] != '\0' && b < n)
	{
		s1[a] = s2[b];
		a++;
		b++;
	}
	s1[a] = '\0';
	return (s1);
}
