#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	a;

	a = 0;
	if (s != '\0' && f != '\0')
	{
		while (s[a] != '\0')
		{
			f(&s[a]);
			a++;
		}
	}
}
